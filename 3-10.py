# This is a practice program using lists and functions

places = ['store', 'mountains', 'plains', 'beach', 'grassland']
#sorted puts the list in alphabetical order
print (sorted(places))
#This reverses the order of the list
places.reverse()
print (places)
#Changes back to the original order
places.reverse()
print (places)
#sorts alphabetically
places.sort()
print (places)
#sorts the list backwards alphabetically
places.reverse()
print (places)